    public void addToolsListener(ToolsListener tl) {
        if (tl == null || toolsListener.indexOf(tl) >= 0)
            return;
        toolsListener.add(tl);
    }
    
    public void removeToolsListener(ToolsListener tl) {
        if (tl == null) return;
        toolsListener.remove(tl);
    }
    
