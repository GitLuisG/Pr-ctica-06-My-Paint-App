            new ArrayList<>();
    
    private int size = 5;
    
    private void notifyToolChanged(String toolName) {
        for (ToolsListener tl : toolsListener)
            tl.toolChanged(new ToolChangedEvent(this, toolName));
    }
    
    private void notifyColorChanged(Color color, String colorName){
        for (ToolsListener tl : toolsListener) {
            ColorChangedEvent e = new ColorChangedEvent(
                    this, color, colorName);
            tl.colorChanged(e);
        }
    }
    
    private void notifySizeChanged(int size) {
        for (ToolsListener tl : toolsListener)
            tl.sizeChanged(new SizeChangedEvent(this, size));
    }
    
